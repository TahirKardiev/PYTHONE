def транспонировать_матрицу(матрица):
    # Получить количество строк и столбцов в исходной матрице
    кол_строк = len(матрица)
    кол_столбцов = len(матрица[0])
    
    # Создать новую матрицу с обратными размерами (столбцы станут строками и наоборот)
    транспонированная_матрица = [[0] * кол_строк for _ in range(кол_столбцов)]
    
    # Заполнить транспонированную матрицу
    for i in range(кол_строк):
        for j in range(кол_столбцов):
            транспонированная_матрица[j][i] = матрица[i][j]
    
    return транспонированная_матрица

# Пример использования
исходная_матрица = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

транспонированная = транспонировать_матрицу(исходная_матрица)

# Вывести транспонированную матрицу
for row in транспонированная:
    print(row)
