def создать_словарь(**kwargs):
    словарь = {}
    for key, value in kwargs.items():
        # Используйте строковое представление ключа, если он не хешируем
        ключ = str(key) if isinstance(key, (int, float, str, bool)) else key
        словарь[ключ] = value
    return словарь

# Пример использования
результат = создать_словарь(
    имя="John",
    возраст=30,
    зарплата=1000,
    адрес=("123 Main St", "City"),
    любимые_цвета=["синий", "зеленый"]
)

print(результат)

